package model;

public class NewVM {
    private int id;
    private int userId;
    private String nameVm;
    private String difficulty;
    private String creator;
    private String urlVm;
    private String userFlag;
    private String rootFlag;
    private String urlWriteup;
    private String contact;
    private String tags;
    private String estado;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getNameVm() {
		return nameVm;
	}
	public void setNameVm(String nameVm) {
		this.nameVm = nameVm;
	}
	public String getDifficulty() {
		return difficulty;
	}
	public void setDifficulty(String difficulty) {
		this.difficulty = difficulty;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public String getUrlVm() {
		return urlVm;
	}
	public void setUrlVm(String urlVm) {
		this.urlVm = urlVm;
	}
	public String getUserFlag() {
		return userFlag;
	}
	public void setUserFlag(String userFlag) {
		this.userFlag = userFlag;
	}
	public String getRootFlag() {
		return rootFlag;
	}
	public void setRootFlag(String rootFlag) {
		this.rootFlag = rootFlag;
	}
	public String getUrlWriteup() {
		return urlWriteup;
	}
	public void setUrlWriteup(String urlWriteup) {
		this.urlWriteup = urlWriteup;
	}
	public String getContact() {
		return contact;
	}
	public void setContact(String contact) {
		this.contact = contact;
	}
	public String getTags() {
		return tags;
	}
	public void setTags(String tags) {
		this.tags = tags;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
}